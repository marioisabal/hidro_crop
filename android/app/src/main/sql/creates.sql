CREATE TABLE IF NOT EXISTS ETAPA (
	BEGIN TRANSACTION;
	idEtapa	INTEGER PRIMARY KEY AUTOINCREMENT,
	nombre	TEXT NOT NULL
);
CREATE TABLE IF NOT EXISTS RIEGO (
	idRiego	INTEGER PRIMARY KEY AUTOINCREMENT,
	tipo	TEXT NOT NULL,
	eficiencia_minima	REAL NOT NULL,
	eficiencia_maxima	REAL NOT NULL
);
CREATE TABLE IF NOT EXISTS CULTIVO (
	idCultivo	INTEGER PRIMARY KEY AUTOINCREMENT,
	tipo	TEXT NOT NULL,
	fecha_siembra_minima	TEXT NOT NULL,
	fecha_siembra_maxima	TEXT NOT NULL
);
CREATE TABLE IF NOT EXISTS CULTIVO_TIENE_ETAPAS (
	idCultivoTieneEtapas	INTEGER PRIMARY KEY AUTOINCREMENT,
	idCultivo	INTEGER NOT NULL,
	idEtapa	INTEGER NOT NULL,
	kc	REAL NOT NULL,
	duracion	INTEGER NOT NULL,
	FOREIGN KEY(idEtapa) REFERENCES ETAPA(idEtapa),
	FOREIGN KEY(idCultivo) REFERENCES CULTIVO(idCultivo)
);
CREATE TABLE IF NOT EXISTS CAMPO (
	idCampo	INTEGER PRIMARY KEY AUTOINCREMENT,
	nombre	TEXT NOT NULL,
	longitud	REAL NOT NULL,
	latitud	REAL NOT NULL,
	fecha_siembra	TEXT NOT NULL,
	idRiego	INTEGER NOT NULL,
	idCultivo	INTEGER NOT NULL,
	FOREIGN KEY(idRiego) REFERENCES RIEGO(idRiego),
	FOREIGN KEY(idCultivo) REFERENCES CULTIVO(idCultivo)
);



